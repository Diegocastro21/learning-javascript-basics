
// Operador de asigancion (  =  )
let a = 1;

// Operador de asignacion compuesto
// Usando la suma
a += 3; // a = a + 3
console.log(a);
// Usando la resta
a -= 2; // a = a - 2
console.log(a);
// Usando la multiplicacion
a *= 8; // a = a * 8
console.log(a);
// Usando la Division
a /= 2; // a = a / 2
console.log(a);
// Usando operador Modulo
a %= 5; // a = a % 5
console.log(a);
// Usando Operador Exponete
a **= 2; // a = a ** 2
console.log(a);