// Operacion de suma
let a = 3, b = 2;
let z = a + b;
console.log("Resultado de la suma --> "+z);
// Operacion de resta
z = a - b;
console.log("Resultado de la resta --> "+z);

// Operacion de multiplicacion
z = a * b;
console.log("Resultado de la multiplicacion --> "+z);

// Operacion de division
z = a / b;
console.log("Resultado de la division --> "+z);

// Operacion residuo de la division
z = a % b;
console.log("Residuo de la division --> "+z);

// Operador de exponente
z = a ** b;
// seria 3 ala 2 que da como resultado 3x3 = 9
console.log("Resultado de operador exponente --> "+z);

